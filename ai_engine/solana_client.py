def health_check() -> dict[str, Any]:
    return {
        "rpc_url": RPC_URL,
        "program_id": PROGRAM_ID_STR or "(not deployed yet)",
        "idl_exists": IDL_PATH.exists(),
        "idl_path": str(IDL_PATH),
        "wallet_path": str(Path(WALLET_PATH).expanduser()),
        "usdc_mint": USDC_MINT_STR,
    }
